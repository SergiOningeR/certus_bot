def get_tickets_list_kb(tickets: list):
    builder = InlineKeyboardBuilder()
    for ticket in tickets:
        builder.button(
            text=f"#{ticket.id} - {ticket.title}",
            callback_data=f"view_ticket_{ticket.id}"
        )
    builder.adjust(1)
    return builder.as_markup()